export const sum = (value1: number, value2: number): number => {
  return value1 + value2;
};

export const subtract = (value1: number, value2: number): number => {
  return value1 - value2;
};


export const multiply = (value1: number, value2: number): number => {
    return value1 * value2;
  };
  
  export const divide = (value1: number, value2: number): number => {
    return value1 / value2;
  };
  